    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int codigo;
        String nombre;
        String apellido;
        String direccion;
        String dedicacion;
        int numHoras;
        float costoHora;

        codigo = Integer.parseInt(txtCodigo.getText());
        nombre = txtNombre.getText();
        apellido = txtApellido.getText();
        direccion = txtDire.getText();
        dedicacion = cbxDedicacion.getSelectedItem().toString();
        numHoras = Integer.parseInt(txtHora.getText());
        costoHora = Float.parseFloat(txtCostoHora.getText());

        Docente docente = new Docente(codigo, nombre, apellido, direccion, dedicacion, numHoras, costoHora);

        lstDatos.append(docente.mostrarDatos());

        limpiarCampos();

    }//GEN-LAST:event_jButton1ActionPerformed

    public void limpiarCampos() {
        txtApellido.setText(null);
        txtCodigo.setText(null);
        txtCostoHora.setText(null);
        txtDire.setText(null);
        txtHora.setText(null);
        txtNombre.setText(null);
        cbxDedicacion.setSelectedIndex(0);
    }

